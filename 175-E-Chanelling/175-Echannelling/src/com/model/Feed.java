package com.model;

public class Feed {

	private String service;
	private String Person_nature;
	private String feed;
	private String hos_location;
	
	
	
	public String getService() {
		return service;
	}
	
	
	public void setService(String service) {
		this.service = service;
	}
	
	
	public String getPerson_nature() {
		return Person_nature;
	}
	
	
	public void setPerson_nature(String person_nature) {
		Person_nature = person_nature;
	}
	
	
	public String getFeed() {
		return feed;
	}
	
	
	public void setFeed(String feed) {
		this.feed = feed;
	}
	
	
	
	public String getHos_location() {
		return hos_location;
	}
	
	
	
	public void setHos_location(String hos_location) {
		this.hos_location = hos_location;
	}
	
	
	
	
	
	
	
	
	
}
